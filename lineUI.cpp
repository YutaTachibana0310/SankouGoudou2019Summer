#include "main.h"
#include "input.h"
#include "lineUI.h"
#include "UIdrawer.h"

//*****************************************************************************
// マクロ定義
//*****************************************************************************
#define SIZE_LINE		(D3DXVECTOR3(SCREEN_WIDTH/2,170.0f,0.0f))
#define POSITION_LINE	(D3DXVECTOR3(SCREEN_WIDTH/2,SCREEN_HEIGHT/10*2,0.0f))

//*****************************************************************************
// コンストラクタ
//*****************************************************************************
Line::Line()
{
	line = new Object();

	line->LoadTexture("data/TEXTURE/UI/interfaceLine.png");

	line->position = POSITION_LINE;
	line->size = SIZE_LINE;
	line->rotation = D3DXVECTOR3(0.0f, 0.0f, 0.0f);

	line->SetColorObject(SET_COLOR_NOT_COLORED);

	line->MakeVertex();
}

//*****************************************************************************
// デストラクタ
//*****************************************************************************
Line::~Line()
{
	delete line;
	line = NULL;
}

//=============================================================================
// 更新処理
//=============================================================================
void Line::Update()
{
}

//=============================================================================
// 描画処理
//=============================================================================
void Line::Draw(void)
{
	line->Draw();
	line->SetVertex();
}
